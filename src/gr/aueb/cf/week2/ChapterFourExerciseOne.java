package gr.aueb.cf.week2;

/**
 * Chapter: 4. Char-for-switch
 * Exercise: 1
 * This program prints 10 stars horizontally
 * and 10 stars vertically
 * @author reyke
 */

public class ChapterFourExerciseOne {

    public static void main(String[] args) {

        // Horizontally:
        for (int i = 1; i <= 10; i++) {
            System.out.print("*");
        }

        // Vertically:
        for (int i = 1; i <= 10; i++) {
            System.out.println("*");
        }
    }
}
